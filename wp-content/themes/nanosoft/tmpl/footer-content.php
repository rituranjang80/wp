<script>
    var payPalUrl = "https://www.paypal.com/checkoutnow?token=1EH56021K4400931F";

    function openLink(url, openInNewTab = false) {
        const link = document.createElement('a');
        link.href = url == "paypal" ? payPalUrl : url;
        link.textContent = url;
        link.style.display = 'none';
        if (openInNewTab) {
            link.target = '_blank';
        }
        document.body.appendChild(link);
        link.click();
    }

    jQuery(document).ready(function($) {
        jQuery(document).ready(function($) {
            $('#newsletter-submit').on('click', function() {
                var email = $('#newsletter-form-email').val().trim();

                // Check if the email field is empty
                if (email === '') {
                    $('#response-message').html('Please enter your email address.');
                    return;
                }

                // Validate email format
                var emailPattern = /^[^@\s]+@[^@\s]+\.[^@\s]+$/;
                if (!emailPattern.test(email)) {
                    $('#response-message').html('Please enter a valid email address.');
                    return;
                }

                // If validation passes, send AJAX request
                $.ajax({
                    url: '<?php echo admin_url('admin-ajax.php'); ?>',
                    type: 'POST',
                    data: {
                        action: 'send_newsletter_email',
                        email: email
                    },
                    success: function(response) {
                        $('#newsletter-form-email').val('');
                        $('#response-message').html(response);
                    },
                    error: function() {
                        $('#response-message').html('An error occurred.');
                    }
                });

                setTimeout(() => {
                    $('#response-message').html('');
                }, 2000);

            });
        });
    });
</script>